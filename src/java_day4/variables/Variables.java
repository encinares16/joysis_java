package java_day4.variables;

public class Variables {
    
    public static void main(String[] args) {
        
        // Declaration
        String name;
        
        // Assignment
        name = "Jerome";
        
        // Initialization
        int age = 25; // or String name = "Jerome";
        System.out.println("Hello, I am " + name + ". I'm " + age + " years old");
    }
}
